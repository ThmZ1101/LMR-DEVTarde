function somar() {
    let n1 = prompt("Digite um nmero","Digite Aqui")
    let n2 = prompt("Digite um nmero","Digite Aqui")
    let n3 = parseInt(n1)+parseInt(n2)
    alert("A somas de ambos é ="+n3)
}
